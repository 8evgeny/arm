void NumberF_7219 (float f) //пишем в левую тетраду c дес€тичной цифрой
{
  int n = (int)(f * 10);
  uint8_t ng=0;//переменна€ дл€ минуса
  if(n<0)
  {
    ng=1;
    n*=-1;
  }
  int m = n;
  uint8_t i=0;
  do
  {
    if(i==1) Send_7219(++i,(n%10) | 0x80);//символ цифры и точка
    else Send_7219(++i,n%10);//символ цифры
    n/=10;
  } while(n);
  if(ng)
  {
    if(m<10)
    {
      Send_7219(i+1,0x80);//ноль с точкой
      Send_7219(i+2,0x0A);//символ -
    }
    else Send_7219(i+1,0x0A);//символ -
  }
  else
  {
    if((m<10)&&(m!=0))
    {
      Send_7219(i+1,0x80);//ноль с точкой
    }
  }
}
